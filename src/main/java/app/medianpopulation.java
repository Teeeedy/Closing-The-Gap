package app;

public class medianpopulation {
    String lgaState;

    int population;

    public medianpopulation(String lgaState, int population) {
        this.lgaState = lgaState;
        this.population = population;
     }
  
     public String getlgaState() {
        return lgaState;
     }
  
     public int getpopulation() {
        return population;
     }
    
}
